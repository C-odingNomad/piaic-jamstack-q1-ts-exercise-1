let alien_color = "green";
if (alien_color === "green") {
  console.log("Congrats!! The player just earned 5 points. ");
} else if (alien_color === "yellow") {
  console.log("The player just earned 10");
} else if (alien_color === "red") {
  console.log("The player just earned 15");
}
